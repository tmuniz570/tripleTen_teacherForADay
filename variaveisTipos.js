// Variável para armazenar um número
let idade = 30;
console.log(idade); // Saída: 30
console.log(typeof idade); // Saída: "number"

// Variável para armazenar um texto (string)
let nome = "Bob";
console.log(nome);  // Saída: Bob
console.log(typeof nome); // Saída: "string"

// Variável para armazenar um valor booleano
let estaChovendo = false;
console.log(estaChovendo); // Saída: false
console.log(typeof estaChovendo); // Saída: "boolean"